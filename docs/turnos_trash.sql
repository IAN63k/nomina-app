-- turnos_trash.sql
-- Papelera para los módulos de Recargos (Médicos y Auxiliares).
--
-- El botón "Eliminar datos de BD" de cada pestaña NO borra de forma definitiva: mueve
-- todas las filas de la tabla principal a su papelera espejo y deja la tabla vacía.
-- La operación es atómica (una sola función SQL `security definer`).
--
-- `turnos_medicos_trash` ya existía; aquí solo se le agrega `fecha_inicio`.
-- `turnos_auxiliares_trash` se crea completa (espejo + `festivo` + metadatos de borrado).
--
-- Ejecutar una sola vez en Supabase (SQL Editor) si se reconstruye el proyecto.

-- 1) Completar la papelera de médicos con fecha_inicio (eje de agregación) para que la
--    restauración sea sin pérdida.
alter table public.turnos_medicos_trash
  add column if not exists fecha_inicio date;

-- 2) Papelera espejo de turnos_auxiliares.
create table if not exists public.turnos_auxiliares_trash (
  id            bigint generated by default as identity primary key,
  original_medico       text,
  original_fecha_inicio date,
  original_concepto     integer,
  medico        text        not null,
  documento     bigint,
  fecha         date        not null,
  fecha_inicio  date,
  turno_codigo  text        default ''::text,
  entrada       text,
  salida        text,
  concepto      integer     not null default 0,
  horas         numeric     default 0,
  horasrecargo  numeric     default 0,
  diferencia    numeric     default 0,
  dia           text,
  mes           integer,
  dia_numero    integer,
  festivo       boolean     default false,
  created_at    timestamptz,
  deleted_by    text,
  deleted_at    timestamptz not null default now()
);

alter table public.turnos_auxiliares_trash enable row level security;

create index if not exists turnos_auxiliares_trash_deleted_at_idx
  on public.turnos_auxiliares_trash (deleted_at desc);
create index if not exists turnos_medicos_trash_deleted_at_idx
  on public.turnos_medicos_trash (deleted_at desc);

-- 3) Mover TODOS los turnos de médicos a la papelera de forma atómica.
create or replace function public.vaciar_turnos_medicos(p_deleted_by text default null)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_count integer;
begin
  with moved as (
    delete from public.turnos_medicos
    returning *
  )
  insert into public.turnos_medicos_trash (
    original_id, medico, documento, fecha, fecha_inicio, turno_codigo, entrada, salida,
    concepto, horas, horasrecargo, diferencia, dia, mes, dia_numero, created_at, deleted_by
  )
  select id, medico, documento, fecha, fecha_inicio, turno_codigo, entrada, salida,
    concepto, horas, horasrecargo, diferencia, dia, mes, dia_numero, created_at, p_deleted_by
  from moved;
  get diagnostics v_count = row_count;
  return v_count;
end;
$$;

-- 4) Mover TODOS los turnos de auxiliares a la papelera de forma atómica.
create or replace function public.vaciar_turnos_auxiliares(p_deleted_by text default null)
