-- 6) Resumen del último lote eliminado (para mostrar/ocultar el botón "Restaurar").
create or replace function public.ultima_eliminacion_turnos_medicos()
returns table(filas bigint, deleted_at timestamptz, deleted_by text)
language sql
security definer
set search_path = public
as $$
  select count(*)::bigint, t.deleted_at, max(t.deleted_by)
  from public.turnos_medicos_trash t
  where t.deleted_at = (select max(deleted_at) from public.turnos_medicos_trash)
  group by t.deleted_at;
$$;

