grant execute on function public.vaciar_turnos_medicos(text)   to anon, authenticated;
grant execute on function public.vaciar_turnos_auxiliares(text) to anon, authenticated;

-- 5) Restaurar el último lote eliminado (el grupo con el deleted_at más reciente) desde
--    la papelera de vuelta a la tabla principal. En conflicto de clave
--    (medico, fecha_inicio, concepto) sobrescribe la fila existente. Atómico.
create or replace function public.restaurar_turnos_medicos()
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_ts    timestamptz;
  v_count integer;
begin
  select max(deleted_at) into v_ts from public.turnos_medicos_trash;
  if v_ts is null then
    return 0;
  end if;

  with restored as (
    delete from public.turnos_medicos_trash
    where deleted_at = v_ts
    returning *
  ), ins as (
    insert into public.turnos_medicos (
      medico, documento, fecha, fecha_inicio, turno_codigo, entrada, salida,
      concepto, horas, horasrecargo, diferencia, dia, mes, dia_numero, created_at
    )
    select medico, documento, fecha, fecha_inicio, turno_codigo, entrada, salida,
      concepto, horas, horasrecargo, diferencia, dia, mes, dia_numero, coalesce(created_at, now())
    from restored
    on conflict (medico, fecha_inicio, concepto) do update set
      documento    = excluded.documento,
      fecha        = excluded.fecha,
      turno_codigo = excluded.turno_codigo,
      entrada      = excluded.entrada,
      salida       = excluded.salida,
      horas        = excluded.horas,
      horasrecargo = excluded.horasrecargo,
      diferencia   = excluded.diferencia,
      dia          = excluded.dia,
      mes          = excluded.mes,
      dia_numero   = excluded.dia_numero
    returning 1
  )
  select count(*) into v_count from restored;
  return v_count;
end;
$$;

